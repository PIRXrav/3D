color CL_rgb(uint8_t r, uint8_t g, uint8_t b) {
  return CL_rgba(r, g, b, CL_ALPHA_OPAQUE);
}

color CL_rgba(uint8_t r, uint8_t g, uint8_t b, uint8_t a) {
  return (color)(struct color_rgb){.b = b, .g = g, .r = r, .a = a};
}

color CL_Random() { return CL_rgb(rand(), rand(), rand()); }

color CL_Negate(color c) {
  return CL_rgba(255 - c.r, 255 - c.g, 255 - c.b, c.a);
}

